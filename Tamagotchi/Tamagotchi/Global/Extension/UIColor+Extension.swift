import UIKit

extension UIColor {
    @nonobjc class var backgroundColor: UIColor {
        return UIColor(red: 245.0 / 255.0, green: 252.0 / 255.0, blue: 252.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var foregroundColor: UIColor {
        return UIColor(red: 77.0 / 255.0, green: 106.0 / 255.0, blue: 120.0 / 255.0, alpha: 1.0)
    }
}
